# TESTE DE RACIOCÍNIO - DIVISÃO CHÃO (FLOOR)
# Regra: Sempre arredonda para o número inteiro à ESQUERDA na reta.

print(-2 / 4)   # Resultado real: -0.5
print(-2 // 4)  # Resultado Python: -1 (Porque -1 < -0.5)

print(2 / 4)    # Resultado real: 0.5
print(2 // 4)   # Resultado Python: 0 (Porque 0 < 0.5)
