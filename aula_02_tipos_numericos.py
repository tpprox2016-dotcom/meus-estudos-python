# --- EXERCÍCIOS DE BASES NUMÉRICAS ---

# Octal (Base 8): Começa com 0o
# 0o123 -> (1*64) + (2*8) + (3*1) = 83
print(0o123) 

# Hexadecimal (Base 16): Começa com 0x
# 0x123 -> (1*256) + (2*16) + (3*1) = 291
print(0x123)

# --- EXERCÍCIOS DE BOOLEANOS ---

# No Python, True vale 1 e False vale 0
print(True > False)  # Resultado esperado: True (1 é maior que 0)
print(True < False)  # Resultado esperado: False (1 não é menor que 0)

# Curiosidade que aprendi:
print(True + True)   # Vai dar 2, porque 1 + 1 = 2
