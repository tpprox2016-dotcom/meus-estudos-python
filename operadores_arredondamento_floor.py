# --- ESTUDO DE DIVISÃO INTEIRA COM NEGATIVOS ---
# Regra: O operador // (floor) sempre arredonda para o MENOR inteiro (esquerda na reta).

# Exemplo 1: 
# -2 / 4 daria -0.5. O vizinho da esquerda é o -1.
print(-2 // 4)  # Resultado: -1

# Exemplo 2:
# -10 / 3 daria -3.33. O vizinho da esquerda é o -4.
print(-10 // 3) # Resultado: -4

# Exemplo 3 (Positivo para comparar):
# 10 / 3 daria 3.33. O vizinho da esquerda é o 3.
print(10 // 3)  # Resultado: 3
